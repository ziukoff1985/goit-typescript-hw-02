import './App.css';
import toast, { Toaster } from 'react-hot-toast'; // –ë—ñ–±–ª—ñ–æ—Ç–µ–∫–∞ –¥–ª—è –≤—ñ–¥–æ–±—Ä–∞–∂–µ–Ω–Ω—è –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω—å
import { useEffect, useState } from 'react'; // React-—Ö—É–∫–∏ –¥–ª—è –∫–µ—Ä—É–≤–∞–Ω–Ω—è —Å—Ç–∞–Ω–æ–º —ñ –ø–æ–±—ñ—á–Ω–∏–º–∏ –µ—Ñ–µ–∫—Ç–∞–º–∏
import { fetchImages } from './services/api'; // –§—É–Ω–∫—Ü—ñ—è –¥–ª—è –∑–∞–ø–∏—Ç—É –∑–æ–±—Ä–∞–∂–µ–Ω—å –∑ API (—Ñ–∞–π–ª ./services/api.js)
import Loader from './components/Loader/Loader'; // –ö–æ–º–ø–æ–Ω–µ–Ω—Ç —ñ–Ω–¥–∏–∫–∞—Ç–æ—Ä–∞ –∑–∞–≤–∞–Ω—Ç–∞–∂–µ–Ω–Ω—è
import ImageGallery from './components/ImageGallery/ImageGallery'; // –ö–æ–º–ø–æ–Ω–µ–Ω—Ç –¥–ª—è –≥–∞–ª–µ—Ä–µ—ó –∑–æ–±—Ä–∞–∂–µ–Ω—å
import SearchBar from './components/SearchBar/SearchBar'; // –ö–æ–º–ø–æ–Ω–µ–Ω—Ç —Ñ–æ—Ä–º–∏ –ø–æ—à—É–∫—É
import ErrorMessage from './components/ErrorMessage/ErrorMessage'; // –ö–æ–º–ø–æ–Ω–µ–Ω—Ç –¥–ª—è –≤—ñ–¥–æ–±—Ä–∞–∂–µ–Ω–Ω—è –ø–æ–º–∏–ª–æ–∫
import LoadMoreBtn from './components/LoadMoreBtn/LoadMoreBtn'; // –ö–Ω–æ–ø–∫–∞ –¥–ª—è –∑–∞–≤–∞–Ω—Ç–∞–∂–µ–Ω–Ω—è –¥–æ–¥–∞—Ç–∫–æ–≤–∏—Ö —Å—Ç–æ—Ä—ñ–Ω–æ–∫
import ImageModal from './components/ImageModal/ImageModal'; // –ú–æ–¥–∞–ª—å–Ω–µ –≤—ñ–∫–Ω–æ –¥–ª—è –ø–µ—Ä–µ–≥–ª—è–¥—É –∑–æ–±—Ä–∞–∂–µ–Ω—å
import ErrorNotification from './components/NoResultsNotification/NoResultsNotification'; // –ü–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è "–Ω—ñ—á–æ–≥–æ –Ω–µ –∑–Ω–∞–π–¥–µ–Ω–æ"
import TypingEffectMessage from './components/TypingEffectMessage/TypingEffectMessage'; // –ö–æ–º–ø–æ–Ω–µ–Ω—Ç –¥–ª—è –∞–Ω—ñ–º–æ–≤–∞–Ω–æ–≥–æ (–¥—Ä—É–∫–æ–≤–∞–Ω–æ–≥–æ —Ç–µ–∫—Å—Ç—É)

// –ì–æ–ª–æ–≤–Ω–∏–π –∫–æ–º–ø–æ–Ω–µ–Ω—Ç –ê—Ä—Ä
function App() {
  // --- –°—Ç–∞–Ω–∏ –¥–æ–¥–∞—Ç–∫–∞ ---
  const [query, setQuery] = useState(''); // –°—Ç–∞–Ω –¥–ª—è –∫–ª—é—á–æ–≤–æ–≥–æ –∑–∞–ø–∏—Ç—É (—â–æ —à—É–∫–∞—î –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á)
  const [images, setImages] = useState([]); // –°—Ç–∞–Ω –¥–ª—è –º–∞—Å–∏–≤—É –∑–æ–±—Ä–∞–∂–µ–Ω—å, –æ—Ç—Ä–∏–º–∞–Ω–∏—Ö –∑ API
  const [isLoading, setIsLoading] = useState(false); // –°—Ç–∞–Ω –¥–ª—è —ñ–Ω–¥–∏–∫–∞—Ç–æ—Ä–∞ –∑–∞–≤–∞–Ω—Ç–∞–∂–µ–Ω–Ω—è (–ª–æ–∞–¥–µ—Ä–∞)
  const [isError, setIsError] = useState(false); // –°—Ç–∞–Ω –¥–ª—è –≤—ñ–¥–æ–±—Ä–∞–∂–µ–Ω–Ω—è –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è –ø—Ä–æ –ø–æ–º–∏–ª–∫—É
  const [page, setPage] = useState(1); // –°—Ç–∞–Ω –¥–ª—è –Ω–æ–º–µ—Ä–∞ –ø–æ—Ç–æ—á–Ω–æ—ó —Å—Ç–æ—Ä—ñ–Ω–∫–∏ (–ø–∞–≥—ñ–Ω–∞—Ü—ñ—è)
  const [modalImage, setModalImage] = useState(''); // –°—Ç–∞–Ω –¥–ª—è URL –∑–æ–±—Ä–∞–∂–µ–Ω–Ω—è, —è–∫–µ –≤—ñ–¥–∫—Ä–∏–≤–∞—î—Ç—å—Å—è —É –º–æ–¥–∞–ª—å–Ω–æ–º—É –≤—ñ–∫–Ω—ñ
  const [isModalOpen, setIsModalOpen] = useState(false); // –°—Ç–∞–Ω –¥–ª—è –∫–æ–Ω—Ç—Ä–æ–ª—é –≤—ñ–¥–∫—Ä–∏—Ç—Ç—è/–∑–∞–∫—Ä–∏—Ç—Ç—è –º–æ–¥–∞–ª—å–Ω–æ–≥–æ –≤—ñ–∫–Ω–∞
  const [isNoResults, setIsNoResults] = useState(false); // –°—Ç–∞–Ω –¥–ª—è –≤—ñ–¥–æ–±—Ä–∞–∂–µ–Ω–Ω—è –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è "–Ω—ñ—á–æ–≥–æ –Ω–µ –∑–Ω–∞–π–¥–µ–Ω–æ"
  const [totalPages, setTotalPages] = useState(0); // –°—Ç–∞–Ω –¥–ª—è –∑–∞–≥–∞–ª—å–Ω–æ—ó –∫—ñ–ª—å–∫–æ—Å—Ç—ñ —Å—Ç–æ—Ä—ñ–Ω–æ–∫, –æ—Ç—Ä–∏–º–∞–Ω–∏—Ö –∑ API
  const [typingMessage, setTypingMessage] = useState(true); // –°—Ç–∞–Ω –¥–ª—è –∞–Ω—ñ–º–æ–≤–∞–Ω–æ–≥–æ —Ç–µ–∫—Å—Ç—É (–∑ –µ—Ñ–µ–∫—Ç–æ–º –¥—Ä—É–∫—É)

  // useEffect –¥–ª—è –∑–∞–ø–∏—Ç—É –∑–æ–±—Ä–∞–∂–µ–Ω—å –∑ API –ø—Ä–∏ –∑–º—ñ–Ω—ñ —Å—Ç–∞–Ω—ñ–≤ `query` –∞–±–æ `page`
  useEffect(() => {
    // –ê—Å–∏–Ω—Ö—Ä–æ–Ω–Ω–∞ —Ñ—É–Ω–∫—Ü—ñ—è –¥–ª—è –∑–∞–ø–∏—Ç—É –¥–∞–Ω–∏—Ö –∑ API
    const fetchImagesData = async () => {
      setIsLoading(true); // –í—ñ–¥–æ–±—Ä–∞–∂–∞—î–º–æ –ª–æ–∞–¥–µ—Ä (—Å—Ç–∞–Ω ‚Üí –∑ false –≤ true)
      setIsError(false); // –°–∫–∏–¥–∞—î–º–æ –ø–æ–º–∏–ª–∫—É, —è–∫—â–æ –≤–æ–Ω–∞ –±—É–ª–∞
      setIsNoResults(false); // –°–∫–∏–¥–∞—î–º–æ —Å—Ç–∞–Ω "–Ω–µ–º–∞—î —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ñ–≤" –≤ false
      setTotalPages(0); // –°–∫–∏–¥–∞—î–º–æ –∫—ñ–ª—å–∫—ñ—Å—Ç—å —Å—Ç–æ—Ä—ñ–Ω–æ–∫ –Ω–∞ –ø–æ—á–∞—Ç–∫–æ–≤–µ –∑–Ω–∞—á–µ–Ω–Ω—è
      setTypingMessage(false); // X–æ–≤–∞—î–º–æ –∞–Ω—ñ–º–æ–≤–∞–Ω–∏–π —Ç–µ–∫—Å—Ç

      // –ë–ª–æ–∫ try...catch –¥–ª—è –≤—ñ–¥–ø—Ä–∞–≤–∫–∏ –∑–∞–ø–∏—Ç—É
      try {
        // –û—Ç—Ä–∏–º—É—î–º–æ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∏ –∑–∞–ø–∏—Ç—É —Ç–∞ –∑–∞–≥–∞–ª—å–Ω—É –∫—ñ–ª—å–∫—ñ—Å—Ç—å —Å—Ç–æ—Ä—ñ–Ω–æ–∫
        const { results, total_pages } = await fetchImages(query, page);
        // –Ø–∫—â–æ –∑–∞–ø–∏—Ç –Ω–µ –¥–∞–≤ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ñ–≤, –∞–∫—Ç–∏–≤—É—î–º–æ –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è "–Ω—ñ—á–æ–≥–æ –Ω–µ –∑–Ω–∞–π–¥–µ–Ω–æ"
        if (results.length === 0) {
          setIsNoResults(true); // –í—Å—Ç–∞–Ω–æ–≤–ª—é—î–º–æ —Å—Ç–∞–Ω "–Ω–µ–º–∞—î —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ñ–≤" –≤ true
          setTypingMessage(true); // –ü–æ–∫–∞–∑—É—î–º–æ –∞–Ω—ñ–º–æ–≤–∞–Ω–∏–π —Ç–µ–∫—Å—Ç –∑ –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è–º
        }
        // –î–æ–¥–∞—î–º–æ –Ω–æ–≤—ñ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∏ –¥–æ —ñ—Å–Ω—É—é—á–∏—Ö (–ø—ñ–¥ —á–∞—Å –ø–∞–≥—ñ–Ω–∞—Ü—ñ—ó) - —á–µ—Ä–µ–∑ prev
        setImages(prevImages => [...prevImages, ...results]);
        setTotalPages(total_pages); // –û–Ω–æ–≤–ª—é—î–º–æ –∑–∞–≥–∞–ª—å–Ω—É –∫—ñ–ª—å–∫—ñ—Å—Ç—å —Å—Ç–æ—Ä—ñ–Ω–æ–∫

        // –Ø–∫—â–æ –¥–æ—Å—è–≥–Ω—É—Ç–æ –æ—Å—Ç–∞–Ω–Ω—å–æ—ó —Å—Ç–æ—Ä—ñ–Ω–∫–∏, –≤–∏–≤–æ–¥–∏–º–æ –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è (–±—ñ–±—ñ–ª—ñ–æ—Ç–µ–∫–∞ react-hot-toast)
        if (page === total_pages) {
          toast.error('Oops, this is the last page ü§∑‚Äç‚ôÇÔ∏è');
        }
      } catch (error) {
        // –ë–ª–æ–∫ –¥–ª—è –æ–±—Ä–æ–±–∫–∏ –º–æ–∂–ª–∏–≤–æ—ó –ø–æ–º–∏–ª–∫–∏
        setIsError(true); // –Ø–∫—â–æ —Å—Ç–∞–ª–∞—Å—è –ø–æ–º–∏–ª–∫–∞, –∞–∫—Ç–∏–≤—É—î–º–æ –≤—ñ–¥–ø–æ–≤—ñ–¥–Ω–∏–π —Å—Ç–∞–Ω
        console.log(error);
      } finally {
        // –ë–ª–æ–∫ finally
        setIsLoading(false); // –ü—Ä–∏—Ö–æ–≤—É—î–º–æ –ª–æ–∞–¥–µ—Ä
      }
    };
    // –í–∏–∫–æ–Ω—É—î–º–æ –∑–∞–ø–∏—Ç —Ç—ñ–ª—å–∫–∏ —è–∫—â–æ —î –ø–æ—à—É–∫–æ–≤–∏–π –∑–∞–ø–∏—Ç (query) –ù–ï –ü–û–†–û–ñ–ù–Ü–ô!!!
    if (query) {
      fetchImagesData(); // –¢—ñ–ª—å–∫–∏ –≤ —Ü—å–æ–º—É —Ä–∞–∑—É –≤–∏–∫–ª–∏–∫–∞—î–º–æ —Ñ—É–Ω–∫—Ü—ñ—é –∑–∞–ø–∏—Ç—É –Ω–∞ –ê–†–Ü (fetchImagesData)
    }
  }, [query, page]); // –ú–∞—Å–∏–≤ –∑–ª–µ–∂–Ω–æ—Å—Ç–µ–π - useEffect —Å–ª—ñ–¥–∫—É—î –∑–∞ –∑–º—ñ–Ω–æ—é —Å—Ç–∞–Ω—ñ–≤ "query" —ñ "page"

  // –§—É–Ω–∫—Ü—ñ—è —è–∫–∞ –∑–∞–ø—É—Å–∫–∞—î—Ç—å—Å—è –ø—Ä–∏ –≤—ñ–¥–ø—Ä–∞–≤—Ü—ñ –∫–ª—é—á–æ–≤–æ–≥–æ –∑–∞–ø–∏—Ç—É (Submit —Ñ–æ—Ä–º–∏ –≤ —Ñ–∞–π–ª—ñ SearchBar.jsx)
  const handleSearchSubmit = newQuery => {
    setQuery(newQuery); // –í—Å—Ç–∞–Ω–æ–≤–ª—é—î–º–æ –Ω–æ–≤–∏–π –ø–æ—à—É–∫–æ–≤–∏–π –∑–∞–ø–∏—Ç (–∑–∞–ø–∏—Ç –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á–∞)
    setImages([]); // –°–∫–∏–¥–∞—î–º–æ —Å—Ç–∞–Ω –∑–æ–±—Ä–∞–∂–µ–Ω—å (–æ—á–∏—â–∞—î–º–æ –≥–∞–ª–µ—Ä–µ—é)
    setPage(1); // –°–∫–∏–¥–∞—î–º–æ –∑–Ω–∞—á–µ–Ω–Ω—è –ø–æ—Ç–æ—á–Ω–æ—ó —Å—Ç–æ—Ä—ñ–Ω–∫–∏ –Ω–∞ "–ø–µ—Ä—à—É" (–ø–æ—á–∏–Ω–∞—î–º–æ –∑ –ø–µ—Ä—à–æ—ó)
  };

  // –§—É–Ω–∫—Ü—ñ—è –¥–ª—è –∑–∞–≤–∞–Ω—Ç–∞–∂–µ–Ω–Ω—è (–ø–∞–≥—ñ–Ω–∞—Ü—ñ—ó) –¥–æ–¥–∞—Ç–∫–æ–≤–∏—Ö —Å—Ç–æ—Ä—ñ–Ω–æ–∫ (Load More)
  const handleLoadMore = () => {
    // –ü–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ —á–∏ –Ω–æ–º–µ—Ä –ø–æ—Ç–æ—á–Ω–æ—ó —Å—Ç–æ—Ä—ñ–Ω–∫–∏ –º–µ–Ω—à–µ –∑–∞–≥–∞–ª—å–Ω–æ—ó –∫—ñ–ª—å–∫–æ—Å—Ç—ñ —Å—Ç–æ—Ä—ñ–Ω–æ–∫ –≤ –∑–∞–ø–∏—Ç—ñ
    if (page < totalPages) {
      setPage(prevPage => prevPage + 1); // –û–Ω–æ–≤–ª—é—î–º–æ —Å—Ç–∞–Ω –ø–æ—Ç–æ—á–Ω–æ—ó —Å—Ç–æ—Ä—ñ–Ω–∫–∏ +1
    }
  };

  // –§—É–Ω–∫—Ü—ñ—è –¥–ª—è –≤—ñ–¥–∫—Ä–∏—Ç—Ç—è –º–æ–¥–∞–ª—å–Ω–æ–≥–æ –≤—ñ–∫–Ω–∞ —ñ–∑ –∑–æ–±—Ä–∞–∂–µ–Ω–Ω—è–º
  const handleImageClick = imageUrl => {
    setModalImage(imageUrl); // –í—Å—Ç–∞–Ω–æ–≤–ª—é—î–º–æ URL –æ–±—Ä–∞–Ω–æ–≥–æ –∑–æ–±—Ä–∞–∂–µ–Ω–Ω—è
    setIsModalOpen(true); // –í—ñ–¥–∫—Ä–∏–≤–∞—î–º–æ –º–æ–¥–∞–ª—å–Ω–µ –≤—ñ–∫–Ω–æ
  };

  return (
    <>
      {/* –§–æ—Ä–º–∞ –ø–æ—à—É–∫—É –¥–ª—è –≤–≤–µ–¥–µ–Ω–Ω—è –∑–∞–ø–∏—Ç—É */}
      <SearchBar onSubmit={handleSearchSubmit} />
      {/* –ö–æ–º–ø–æ–Ω–µ–Ω—Ç –¥–ª—è –≤—ñ–¥–æ–±—Ä–∞–∂–µ–Ω–Ω—è –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω—å (react-hot-toast) */}
      <Toaster position="top-right" reverseOrder={false} />
      {/* –ê–Ω—ñ–º–æ–≤–∞–Ω–∏–π —Ç–µ–∫—Å—Ç (–Ω–∞ –≥–æ–ª–æ–≤–Ω—ñ–π —Å—Ç–æ—Ä—ñ–Ω—Ü—ñ) */}
      {typingMessage && <TypingEffectMessage />}
      {/* –ì–∞–ª–µ—Ä–µ—è –∑–æ–±—Ä–∞–∂–µ–Ω—å */}
      {images.length > 0 && (
        <ImageGallery images={images} onImageClick={handleImageClick} />
      )}
      {/* –Ü–Ω–¥–∏–∫–∞—Ç–æ—Ä –∑–∞–≤–∞–Ω—Ç–∞–∂–µ–Ω–Ω—è (–ª–æ–∞–¥–µ—Ä) */}
      {isLoading && <Loader />}
      {/* –ü–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è –ø—Ä–æ –ø–æ–º–∏–ª–∫—É */}
      {isError && <ErrorMessage />}
      {/* –ü–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è –ø—Ä–æ –≤—ñ–¥—Å—É—Ç–Ω—ñ—Å—Ç—å —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ñ–≤ */}
      {isNoResults && <ErrorNotification />}
      {/* –ö–Ω–æ–ø–∫–∞ –¥–ª—è –∑–∞–≤–∞–Ω—Ç–∞–∂–µ–Ω–Ω—è –¥–æ–¥–∞—Ç–∫–æ–≤–∏—Ö –∑–æ–±—Ä–∞–∂–µ–Ω—å (Load More) */}
      {images.length > 0 && page < totalPages && (
        <LoadMoreBtn onClick={handleLoadMore} />
      )}
      {/* –ú–æ–¥–∞–ª—å–Ω–µ –≤—ñ–∫–Ω–æ –¥–ª—è –ø–µ—Ä–µ–≥–ª—è–¥—É –∑–æ–±—Ä–∞–∂–µ–Ω–Ω—è */}
      <ImageModal
        isOpen={isModalOpen}
        onRequestClose={() => setIsModalOpen(false)}
        image={modalImage}
      />
    </>
  );
}

export default App;
